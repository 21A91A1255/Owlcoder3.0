class Solution {
  public:
    // Function to split a linked list into two lists alternately
    vector<Node*> alternatingSplitList(struct Node* head) {
        Node* f=head;
        Node* s=head->next;
        vector<Node*>p;
        p.push_back(f);
        p.push_back(s);
        while(f!=NULL && s!=NULL)
        {
            f->next=s->next;
            f=f->next;
            if(f!=NULL)
            {
                s->next=f->next;
                s=s->next;
                
            }
            else{
                s->next=NULL;
            }
        }
        if(f!=NULL) 
            f->next=NULL;
        if(s!=NULL)
            s->next=NULL;
        return p;
    }
};


